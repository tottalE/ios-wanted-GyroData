import UIKit

extension UIFont {
    
    var customBodyFont: UIFont {
        return UIFont(name: "establishRoomNo703OTF", size: 15) ?? UIFont()
    }
    
    var customTitleFont: UIFont {
        return UIFont(name: "establishRoomNo703OTF", size: 32) ?? UIFont()
    }
    
    var customLargeTitleFont: UIFont {
        return UIFont(name: "establishRoomNo703OTF", size: 42) ?? UIFont()
    }
}
